export interface Theme {
  name: string;

  // Surfaces
  bgBase: string;
  bgRaised: string;
  bgSurface: string;
  bgElevated: string;
  bgHeader: string;
  bgInput: string;
  bgHover: string;
  bgSelected: string;
  bgTerminal: string;

  // Borders
  border: string;
  borderSubtle: string;
  borderInput: string;

  // Text
  textPrimary: string;
  textSecondary: string;
  textTertiary: string;
  textMuted: string;
  textFaint: string;
  textDisabled: string;

  // Accent
  accent: string;
  accentText: string;
  accentGlow: string;
  accentBg: string;

  // Status
  success: string;
  error: string;
  warning: string;

  // Overlay / shadow
  overlay: string;
  shadow: string;

  // Scrollbar
  scrollbarThumb: string;
  scrollbarHover: string;

  // Claude pane
  claudeBg: string;
  claudeUserBubble: string;
  claudeUserBorder: string;
  claudeDivider: string;
  claudeBorder: string;
  claudeBorderSubtle: string;

  // Terminal
  terminal: TerminalTheme;
}

